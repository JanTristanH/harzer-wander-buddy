#!/usr/bin/env node

import fs from 'node:fs/promises';
import path from 'node:path';
import sharp from 'sharp';

const rootDir = process.cwd();
const templatePath = path.join(rootDir, 'assets', 'images', 'map', 'marker.svg');
const generatedAssetsParent = path.join(rootDir, 'assets', 'images', 'map', 'generated');
const generatedMappingFilePath = path.join(rootDir, 'lib', 'map-marker-images.generated.ts');

const STAMP_MIN = 1;
const STAMP_MAX = 222;
const BASE_MARKER_WIDTH = 48;
const BASE_MARKER_HEIGHT = 52;
const MARKER_ASSET_SCALES = [1, 2, 3];

const PLACEHOLDERS = {
  fill: '__FILL_COLOR__',
  label: '__LABEL_TEXT__',
  textColor: '__LABEL_COLOR__',
  fontSize: '__LABEL_FONT_SIZE__',
  y: '__LABEL_Y__',
  x: '__LABEL_X__',
  badgeRx: '__BADGE_RX__',
};

const markerVariants = [
  {
    kind: 'visited-stamp',
    fillColor: '#2e6b4b',
    textColor: '#111111',
    labels: [
      '--',
      ...Array.from({ length: STAMP_MAX - STAMP_MIN + 1 }, (_, index) => String(index + STAMP_MIN)),
    ],
  },
  {
    kind: 'open-stamp',
    fillColor: '#c1a093',
    textColor: '#111111',
    labels: [
      '--',
      ...Array.from({ length: STAMP_MAX - STAMP_MIN + 1 }, (_, index) => String(index + STAMP_MIN)),
    ],
  },
  {
    kind: 'parking',
    fillColor: '#2f7dd7',
    textColor: '#111111',
    labels: ['P'],
  },
];

function escapeXmlText(value) {
  return value
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function resolveLabelStyle(label) {
  const length = label.length;
  const consistentFontSize = '14';

  if (length <= 1) {
    return { fontSize: consistentFontSize, x: '28', y: '23.5', badgeRx: '8.6' };
  }

  if (length === 2) {
    return { fontSize: consistentFontSize, x: '28', y: '23.5', badgeRx: '10.9' };
  }

  if (length === 3) {
    return { fontSize: consistentFontSize, x: '28', y: '23.5', badgeRx: '12.8' };
  }

  return { fontSize: consistentFontSize, x: '28', y: '23.5', badgeRx: '14.6' };
}

function filenameForLabel(label) {
  if (label === '--') {
    return 'fallback';
  }

  if (/^\d+$/.test(label)) {
    return label.padStart(3, '0');
  }

  return label.toLowerCase();
}

function renderSvg(template, { fillColor, textColor, label }) {
  const safeLabel = label.trim() || '--';
  const escapedLabel = escapeXmlText(safeLabel);
  const labelStyle = resolveLabelStyle(safeLabel);

  return template
    .replaceAll(PLACEHOLDERS.fill, fillColor)
    .replaceAll(PLACEHOLDERS.textColor, textColor)
    .replaceAll(PLACEHOLDERS.label, escapedLabel)
    .replaceAll(PLACEHOLDERS.fontSize, labelStyle.fontSize)
    .replaceAll(PLACEHOLDERS.x, labelStyle.x)
    .replaceAll(PLACEHOLDERS.y, labelStyle.y)
    .replaceAll(PLACEHOLDERS.badgeRx, labelStyle.badgeRx);
}

function validateTemplate(template) {
  for (const placeholder of Object.values(PLACEHOLDERS)) {
    if (!template.includes(placeholder)) {
      throw new Error(`SVG template is missing placeholder: ${placeholder}`);
    }
  }
}

function buildRecord(entries) {
  return entries
    .sort((left, right) => left.key.localeCompare(right.key))
    .map(({ key, relativeAssetPath }) => {
      const normalizedAssetPath = relativeAssetPath.split(path.sep).join('/');
      return `  '${key}': require('../${normalizedAssetPath}')`;
    })
    .join(',\n');
}

function createMappingFile(entries) {
  const markerRecord = buildRecord(entries);

  return `/* eslint-disable */
// This file is auto-generated by scripts/generate-map-marker-pngs.mjs
// Do not edit manually.

import { type ImageRequireSource } from 'react-native';

type MarkerVisualKind = 'visited-stamp' | 'open-stamp' | 'parking' | 'parking-order' | 'tour-order';

const MAP_MARKER_IMAGE_SOURCE_BY_KEY: Record<string, ImageRequireSource> = {
${markerRecord}
};

function normalizeStampLabel(label: string) {
  const trimmed = label.trim().toUpperCase();
  if (trimmed === '--') {
    return '--';
  }

  if (/^\\d+$/.test(trimmed)) {
    const parsed = Number.parseInt(trimmed, 10);
    if (parsed >= ${STAMP_MIN} && parsed <= ${STAMP_MAX}) {
      return String(parsed);
    }
  }

  return null;
}

export function getPreGeneratedMapMarkerImageSource(input: {
  kind: MarkerVisualKind;
  label: string;
}): ImageRequireSource | undefined {
  const sourceMap = MAP_MARKER_IMAGE_SOURCE_BY_KEY;

  if (input.kind === 'parking') {
    return sourceMap['parking:P'];
  }

  const normalizedStampLabel = normalizeStampLabel(input.label);
  if (!normalizedStampLabel) {
    // Unknown labels should use the runtime text marker fallback.
    return undefined;
  }

  return sourceMap[\`${'${input.kind}'}:${'${normalizedStampLabel}'}\`];
}

export function getPreGeneratedMapMarkerFallbackImageSource(
  kind: MarkerVisualKind = 'open-stamp'
): ImageRequireSource {
  const sourceMap = MAP_MARKER_IMAGE_SOURCE_BY_KEY;

  if (kind === 'parking') {
    return sourceMap['parking:P'];
  }

  return sourceMap[\`${'${kind}'}:--\`] ?? sourceMap['open-stamp:--'];
}
`;
}

async function ensureCleanDirectory(directoryPath) {
  await fs.rm(directoryPath, { recursive: true, force: true });
  await fs.mkdir(directoryPath, { recursive: true });
}

async function main() {
  if (MARKER_ASSET_SCALES.some((scale) => !Number.isFinite(scale) || scale <= 0)) {
    throw new Error(`Invalid marker asset scales: ${MARKER_ASSET_SCALES.join(', ')}`);
  }

  const svgTemplate = await fs.readFile(templatePath, 'utf8');
  validateTemplate(svgTemplate);

  await ensureCleanDirectory(generatedAssetsParent);

  const mappingEntries = [];

  for (const variant of markerVariants) {
    const kindDir = path.join(generatedAssetsParent, variant.kind);
    await fs.mkdir(kindDir, { recursive: true });

    for (const label of variant.labels) {
      const filenameBase = filenameForLabel(label);
      const baseAssetPath = path.join(kindDir, `${filenameBase}.png`);
      const relativeAssetPath = path.relative(rootDir, baseAssetPath);

      const renderedSvg = renderSvg(svgTemplate, {
        fillColor: variant.fillColor,
        textColor: variant.textColor,
        label,
      });

      for (const scale of MARKER_ASSET_SCALES) {
        const scaleSuffix = scale === 1 ? '' : `@${scale}x`;
        const assetPath = path.join(kindDir, `${filenameBase}${scaleSuffix}.png`);
        const outputWidth = Math.round(BASE_MARKER_WIDTH * scale);
        const outputHeight = Math.round(BASE_MARKER_HEIGHT * scale);

        await sharp(Buffer.from(renderedSvg))
          .resize(outputWidth, outputHeight, {
            fit: 'fill',
          })
          .png({ compressionLevel: 9, adaptiveFiltering: true })
          .toFile(assetPath);
      }

      mappingEntries.push({
        key: `${variant.kind}:${label}`,
        relativeAssetPath,
      });
    }
  }

  const mappingFileContent = createMappingFile(mappingEntries);
  await fs.writeFile(generatedMappingFilePath, mappingFileContent, 'utf8');

  console.log(
    `Generated ${mappingEntries.length} marker PNGs and mapping file at ${path.relative(rootDir, generatedMappingFilePath)}`
  );
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
